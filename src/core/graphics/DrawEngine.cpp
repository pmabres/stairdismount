    void DrawEngine::draw()
    {
        static float grey;
        grey += 0.01f;
        if (grey > 1.0f)
        {
            grey = 0.0f;
        }
        //glClearColor(grey, grey, grey, 1.0f);

        glClear( GL_DEPTH_BUFFER_BIT | GL_COLOR_BUFFER_BIT);

        // White side - BACK
        glBegin(GL_POLYGON);
        glColor3f(   1.0,  1.0, 1.0 );
        glVertex3f(  0.5, -0.5, 0.5 );
        glVertex3f(  0.5,  0.5, 0.5 );
        glVertex3f( -0.5,  0.5, 0.5 );
        glVertex3f( -0.5, -0.5, 0.5 );
        glEnd();

// Purple side - RIGHT
        glBegin(GL_POLYGON);
        glColor3f(  1.0,  0.0,  1.0 );
        glVertex3f( 0.5, -0.5, -0.5 );
        glVertex3f( 0.5,  0.5, -0.5 );
        glVertex3f( 0.5,  0.5,  0.5 );
        glVertex3f( 0.5, -0.5,  0.5 );
        glEnd();

// Green side - LEFT
        glBegin(GL_POLYGON);
        glColor3f(   0.0,  1.0,  0.0 );
        glVertex3f( -0.5, -0.5,  0.5 );
        glVertex3f( -0.5,  0.5,  0.5 );
        glVertex3f( -0.5,  0.5, -0.5 );
        glVertex3f( -0.5, -0.5, -0.5 );
        glEnd();

// Blue side - TOP
        glBegin(GL_POLYGON);
        glColor3f(   0.0,  0.0,  1.0 );
        glVertex3f(  0.5,  0.5,  0.5 );
        glVertex3f(  0.5,  0.5, -0.5 );
        glVertex3f( -0.5,  0.5, -0.5 );
        glVertex3f( -0.5,  0.5,  0.5 );
        glEnd();

// Red side - BOTTOM
        glBegin(GL_POLYGON);
        glColor3f(   1.0,  0.0,  0.0 );
        glVertex3f(  0.5, -0.5, -0.5 );
        glVertex3f(  0.5, -0.5,  0.5 );
        glVertex3f( -0.5, -0.5,  0.5 );
        glVertex3f( -0.5, -0.5, -0.5 );
        glEnd();

//
//        glMatrixMode(GL_MODELVIEW);
//        glPushMatrix();
//        gluLookAt
//        glMultMatrix(o.transformation);



        glMatrixMode(GL_MODELVIEW);
        glPopMatrix();
        glLoadIdentity();
        mCamera.render();
        glFlush();
//        mCamera.moveForward(0.001f);
//        mCamera.rotateX(0.03f);
//        mCamera.rotateY(0.02f);
    }

    void DrawEngine::resize(const sf::Uint32& width, const sf::Uint32& height)
    {
        glViewport(0, 0, width, height);
    }

    void DrawEngine::configure()
    {
        glEnable(GL_DEPTH_TEST);
        // Accept fragment if it closer to the camera than the former one
        glDepthFunc(GL_LESS);

        // Cull triangles which normal is not towards the camera
        glEnable(GL_CULL_FACE);
    }
